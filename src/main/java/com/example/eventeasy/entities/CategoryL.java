package com.example.eventeasy.entities;


public class CategoryL {

    private int id;
    private String nom;


    public CategoryL() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }


    public String toString() {
        return nom;
    }
}